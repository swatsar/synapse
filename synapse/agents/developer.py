"""Developer Agent — autonomous skill code generation.

Protocol Version: 1.0
Specification: 3.1

Implements the full CreateSkill pipeline:
  1. Receive task description from Critic/Orchestrator
  2. Build structured LLM prompt with BaseSkill interface
  3. Call LLM → parse Python class from response
  4. AST security scan (block dangerous patterns)
  5. Generate unit tests
  6. Return GeneratedSkill ready for SkillRegistry

On every step an audit event is emitted.
"""
import ast
import hashlib
import logging
import re
import textwrap
import uuid
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional

from synapse.observability.logger import audit

PROTOCOL_VERSION: str = "1.0"
SPEC_VERSION: str = "3.1"
logger = logging.getLogger(__name__)

# Dangerous AST node types / names that must be blocked
BLOCKED_BUILTINS = {
    "eval", "exec", "compile", "__import__", "breakpoint",
    "open",        # allowed only via fs-capability, not raw
    "input",
}
BLOCKED_MODULES = {
    "os", "sys", "subprocess", "shutil", "socket",
    "ctypes", "pickle", "marshal", "shelve",
    "importlib", "builtins", "__builtins__",
}


def _safe_exec(code_obj, namespace: dict) -> None:
    """Execute compiled skill code in an isolated namespace.

    This function exists to provide a single, audited point where dynamic
    code execution happens. Called ONLY after AST security scan passes.

    Bandit B102 is suppressed here: exec() is intentional, controlled,
    and runs in an isolated namespace dict per SYSTEM_SPEC_v3.1 §4.
    """
    exec(code_obj, namespace)  # nosec B102


@dataclass
class GeneratedSkill:
    """A skill produced by DeveloperAgent — not yet active."""
    skill_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    name: str = ""
    version: str = "1.0.0"
    description: str = ""
    code: str = ""
    tests: str = ""
    required_capabilities: List[str] = field(default_factory=list)
    risk_level: int = 2
    security_issues: List[str] = field(default_factory=list)
    passed_ast_scan: bool = False
    protocol_version: str = PROTOCOL_VERSION

    def to_dict(self) -> Dict[str, Any]:
        return {
            "skill_id": self.skill_id,
            "name": self.name,
            "version": self.version,
            "description": self.description,
            "code_length": len(self.code),
            "tests_length": len(self.tests),
            "required_capabilities": self.required_capabilities,
            "risk_level": self.risk_level,
            "security_issues": self.security_issues,
            "passed_ast_scan": self.passed_ast_scan,
            "protocol_version": self.protocol_version,
        }


